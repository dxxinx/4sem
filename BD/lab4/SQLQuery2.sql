use V_MyBASE
--1--
select Товары.Наименование[Товар], Заказы.Номер_заказа[Номер заказа]
from Товары inner join Заказы
on Товары.Наименование = Заказы.Наименование_товара
--2--
select Товары.Наименование[Товар], Заказы.Номер_заказа[Номер заказа]
from Товары inner join Заказы
on Товары.Наименование = Заказы.Наименование_товара where Товары.Наименование like '%Телефон%'
--3--
select distinct
    Заказы.Номер_заказа[Номер заказа],
    Заказы.Наименование_товара[Товар],
    Заказы.Количество[Количество],
    Заказчики.Наименование_фирмы[Заказчик],
    case
        when Заказы.Количество = 1 then 'один'
        when Заказы.Количество = 2 then 'два'
        when Заказы.Количество = 3 then 'три'
        else 'другое количество'
    end as [Количество (текст)]
from Заказы
inner join Заказчики
    on Заказы.Заказчик = Заказчики.Наименование_фирмы
inner join Товары
    on Товары.Наименование = Заказы.Наименование_товара
where Заказы.Количество between 1 and 5
order by Заказы.Количество desc;
--4--
select 
    Заказчики.Наименование_фирмы[Фирма],
    isnull(Заказы.Номер_заказа, 0)[Номер заказа]
from Заказчики
left join Заказы
    on Заказчики.Наименование_фирмы = Заказы.Заказчик;
--5--
select * from Товары
full outer join Заказы
    on Товары.Наименование = Заказы.Наименование_товара
where Заказы.Номер_заказа is null;

SELECT * FROM Заказы
FULL OUTER JOIN Товары
    ON Товары.Наименование = Заказы.Наименование_товара
WHERE Товары.Наименование IS NULL;

SELECT * FROM Заказы
FULL OUTER JOIN Товары
    ON Товары.Наименование = Заказы.Наименование_товара
WHERE Товары.Наименование IS NOT NULL
  AND Заказы.Наименование_товара IS NOT NULL;
--6--
